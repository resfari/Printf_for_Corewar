#include "libft.h"

char	*ft_strlowcase(char *str)
{
	char *str2;

	str2 = str;
	while (*str2 != 0)
	{
		if (*str2 >= 65 && *str2 <= 90)
			*str2 += ' ';
		str2++;
	}
	return (str);
}
