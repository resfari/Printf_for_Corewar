#include "libft.h"

char	*ft_strupcase(char *str)
{
	char *str2;

	str2 = str;
	while (*str2 != 0)
	{
		if (*str2 >= 97 && *str2 <= 122)
			*str2 -= ' ';
		str2++;
	}
	return (str);
}
