#include "libft.h"

void	ft_reverse(char *s)
{
	int		i;
	int		j;
	int		len;
	char	c;

	i = 0;
	len = ft_strlen(s);
	j = len - 1;
	while (i < (len + 1) / 2)
	{
		c = s[i];
		s[i] = s[j];
		s[j] = c;
		i++;
		j--;
	}
}
