#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*z1;
	char	*z2;
	size_t	i;

	z1 = s1;
	while (*z1 != '\0')
		z1++;
	z2 = (char*)s2;
	i = 0;
	while (i < n && *z2 != '\0')
	{
		*z1 = *z2;
		z1++;
		z2++;
		i++;
	}
	*z1 = '\0';
	return (s1);
}
