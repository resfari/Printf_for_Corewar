#include "libft.h"

int		ft_len_before_delim(const char *s, char c)
{
	char *s2;

	if (!s)
		return (-1);
	s2 = (char*)s;
	while (*s2 && *s2 != c)
		s2++;
	if (*s2 != c)
		return (-1);
	return (s2 - s);
}
